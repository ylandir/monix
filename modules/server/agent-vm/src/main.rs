// Guest task supervisor for agent-fleet workers (see docs/agent-fleet.md and
// agent-vm.mod.nix). Runs as guest root inside a disposable, network-contained
// microVM: waits for the host drainer to deliver a task into the virtiofs
// exchange, validates the host-staged canonical metadata and the exactly-one
// selected credential, prepares the cockpit-built context capsule as the
// selected unprivileged executor, runs the provider executor (or the fixed
// deterministic verifier), captures the workspace patch, distills a
// root-produced usage record, and publishes the exit code LAST.
//
// Trust model: guest root is orchestration authority; executors and verifier
// checks are unprivileged users. Everything on the task share and inside the
// context capsule is hostile — no symlink following, bounded reads, no shell
// interpolation. Fixed external tools (runuser, tar, git, jq, sqlite3, cp,
// chown, pkill) do the extraction/execution legwork; all orchestration,
// validation, lifecycle, and publication decisions live here.

