// Trusted usage extraction. Root distills the executor's own usage records
// (Claude/codex: JSONL transcripts; opencode: SQLite) into one normalized
// record. Strictly best-effort: a missing or unparsable store must never
// fail the task. Field names: input/output are totals (output includes
// reasoning); cache_read and cache_creation follow Anthropic's split, and
// codex's cached_input maps to cache_read.

const CLAUDE_USAGE_FILTER: &str = r#"
  [ .[] | select(.type=="assistant" and .message.usage != null)
    | {id: ((.requestId//"") + "/" + (.message.id//"")), u: .message.usage, m: .message.model} ]
  | unique_by(.id)
  | select(length > 0)
  | { executor: "claude",
      model: ((map(.m) | map(select(. != null)) | last) // "unknown"),
      input_tokens: (map(.u.input_tokens//0) | add),
      output_tokens: (map(.u.output_tokens//0) | add),
      cache_read_tokens: (map(.u.cache_read_input_tokens//0) | add),
      cache_creation_tokens: (map(.u.cache_creation_input_tokens//0) | add) }"#;

const CODEX_SESSION_FILTER: &str = r#"
  { m: ([ .[] | select(.type=="turn_context") | .payload.model ] | last),
    t: ([ .[] | select(.type=="event_msg" and .payload.type=="token_count"
                and .payload.info.total_token_usage != null)
          | .payload.info.total_token_usage ] | last) }
  | select(.t != null)"#;

// OpenAI input_tokens INCLUDES cached; normalize to the Anthropic
// convention (input = uncached).
const CODEX_COMBINE_FILTER: &str = r#"
  select(length > 0)
  | { executor: "codex",
      model: ((map(.m) | map(select(. != null)) | last) // "unknown"),
      input_tokens: ((map(.t.input_tokens//0) | add) - (map(.t.cached_input_tokens//0) | add)),
      output_tokens: (map(.t.output_tokens//0) | add),
      cache_read_tokens: (map(.t.cached_input_tokens//0) | add),
      cache_creation_tokens: 0 }"#;

const OPENCODE_USAGE_FILTER: &str = r#"
  [ .[] | .data | fromjson | select(.role=="assistant" and .tokens != null) ]
  | select(length > 0)
  | { executor: "opencode",
      model: (((map(.providerID) | map(select(. != null)) | last) // "?") + "/"
              + ((map(.modelID) | map(select(. != null)) | last) // "unknown")),
      input_tokens: (map(.tokens.input//0) | add),
      output_tokens: ((map(.tokens.output//0) | add) + (map(.tokens.reasoning//0) | add)),
      cache_read_tokens: (map(.tokens.cache.read//0) | add),
      cache_creation_tokens: (map(.tokens.cache.write//0) | add) }"#;

fn claude_usage(home: &Path, executor: Option<&Executor>) -> Option<String> {
    let mut transcripts = Vec::new();
    walk_files(
        &home.join(".claude/projects"),
        ".jsonl",
        &mut transcripts,
        0,
    );
    jq_filter(
        &["-cs", CLAUDE_USAGE_FILTER],
        &concatenate_files(&transcripts),
        executor,
    )
}

// total_token_usage is cumulative per session file: take each file's final
// value, then sum the files.
fn codex_usage(home: &Path, executor: Option<&Executor>) -> Option<String> {
    let mut sessions = Vec::new();
    walk_files(&home.join(".codex/sessions"), ".jsonl", &mut sessions, 0);
    let mut finals = String::new();
    let mut input_budget = USAGE_MAX_BYTES;
    for session in &sessions {
        if input_budget == 0 {
            break;
        }
        if let Ok(file) = open_nofollow(session) {
            let mut text = String::new();
            if file.take(input_budget).read_to_string(&mut text).is_ok()
                && let Some(value) = jq_filter(&["-cs", CODEX_SESSION_FILTER], &text, executor)
            {
                finals.push_str(&value);
                finals.push('\n');
            }
            input_budget = input_budget.saturating_sub(text.len() as u64);
        }
    }
    jq_filter(&["-cs", CODEX_COMBINE_FILTER], &finals, executor)
}

fn opencode_usage(home: &Path, executor: Option<&Executor>) -> Option<String> {
    let store = home.join(".local/share/opencode");
    let database = matching_files(&store, |name| name.ends_with(".db"))
        .into_iter()
        .next()?;
    let metadata = open_nofollow(&database).ok()?.metadata().ok()?;
    if !metadata.file_type().is_file() || metadata.len() > USAGE_MAX_BYTES {
        return None;
    }
    let rows = command_output_bounded(
        tool_command(executor, "sqlite3")
            .args(["-readonly", "-json"])
            .arg(&database)
            .arg("select data from message")
            .stdin(Stdio::null()),
        USAGE_MAX_BYTES as usize,
    )
    .ok()?;
    if !rows.status.success() {
        return None;
    }
    jq_filter(
        &["-c", OPENCODE_USAGE_FILTER],
        &String::from_utf8_lossy(&rows.stdout),
        executor,
    )
}

fn extract_usage(executor: &Executor) -> Option<String> {
    let home = Path::new(executor.home);
    match executor.kind {
        ExecutorKind::Claude => claude_usage(home, Some(executor)),
        ExecutorKind::Codex => codex_usage(home, Some(executor)),
        ExecutorKind::Opencode | ExecutorKind::Local => opencode_usage(home, Some(executor)),
    }
}

// ---------------------------------------------------------------------------
// Context preparation. The capsule is an opaque cockpit-built archive,
// extracted only here, as the selected unprivileged user.

#[derive(Clone, Debug, Default)]
struct ContextInfo {
    baseline: String,
}

struct Supervisor {
    config: Config,
}

impl Supervisor {
    fn prepare_context(&self, executor: &Executor, log: &mut String) -> Result<ContextInfo> {
        let config = &self.config;
        let user = executor.user;
        let owner = format!("{user}:users");
        // The volume mount can supersede tmpfiles-created metadata; repair
        // the shared workspace mode after mounts are live.
        ensure_dir(&config.workspace, 0o770, Some(&owner))?;
        ensure_dir(&config.context_root, 0o700, Some(&owner))?;
        run_logged(
            runuser_command(user, executor.home)
                .args(["tar", "--extract", "--zstd"])
                .args([
                    "--no-same-owner",
                    "--no-same-permissions",
                    "--no-overwrite-dir",
                ])
                .arg("--directory")
                .arg(&config.context_root)
                .arg("--file")
                .arg(config.task("context.tar.zst")),
            log,
        )?;

        let mut info = ContextInfo::default();
        let mut source = config.context_root.as_os_str().to_os_string();
        source.push("/.");
        run_logged(
            runuser_command(user, executor.home)
                .args(["cp", "-a"])
                .arg(source)
                .arg(&config.workspace),
            log,
        )?;

        let git = |args: &[&str]| {
            let mut command = runuser_command(user, executor.home);
            command
                .arg("git")
                .arg("-C")
                .arg(&config.workspace)
                .args(args);
            command
        };
        run_logged(&mut git(&["init", "--quiet"]), log)?;
        run_logged(&mut git(&["config", "user.name", user]), log)?;
        run_logged(
            &mut git(&["config", "user.email", &format!("{user}@agents.invalid")]),
            log,
        )?;
        run_logged(&mut git(&["add", "-A"]), log)?;
        run_logged(
            &mut git(&[
                "commit",
                "--quiet",
                "--allow-empty",
                "-m",
                "fleet context baseline",
            ]),
            log,
        )?;
        info.baseline = run_captured(&mut git(&["rev-parse", "HEAD"]), log)?
            .trim_end()
            .to_string();
        Ok(info)
    }

    // -----------------------------------------------------------------------
    // Credential installation: exactly the selected executor's credential,
    // into that executor's private location, 0400, nothing broader.

    fn credential_entries(&self) -> Vec<CredentialEntry> {
        let Ok(entries) = fs::read_dir(&self.config.creds_dir) else {
            return Vec::new();
        };
        let mut found: Vec<CredentialEntry> = entries
            .flatten()
            .filter(|entry| entry.file_name() != "task-meta")
            .map(|entry| CredentialEntry {
                name: entry.file_name().to_string_lossy().into_owned(),
                regular: is_regular_nofollow(&entry.path()),
            })
            .collect();
        found.sort_by(|left, right| left.name.cmp(&right.name));
        found
    }

    // One credential file into the executor's private location: 0700 dir,
    // 0400 file, owned by that executor, nothing broader.
    fn install_secret(&self, directory: &Path, file: &str, owner: &str, contents: &[u8]) -> Result<()> {
        ensure_dir(directory, 0o700, Some(owner))?;
        let destination = directory.join(file);
        write_replace(&destination, contents, 0o400)?;
        chown_name(&destination, owner)
    }

    fn install_credential(&self, rule: CredentialRule) -> Result<()> {
        let CredentialRule::Exactly(name) = rule else {
            return Ok(());
        };
        let secret = read_bounded(&self.config.creds_dir.join(name), CREDENTIAL_MAX_BYTES)?;
        let env_line = |variable: &str| {
            format!(
                "export {variable}={}\n",
                shell_quote(trim_trailing_newlines(&secret))
            )
        };
        match name {
            "claude-token" => self.install_secret(
                Path::new("/run/agent-claude"),
                "env",
                "agent-claude:users",
                env_line("CLAUDE_CODE_OAUTH_TOKEN").as_bytes(),
            ),
            "codex-auth.json" => self.install_secret(
                Path::new("/home/agent-codex/.codex"),
                "auth.json",
                "agent-codex:users",
                secret.as_bytes(),
            ),
            "openrouter-key" => self.install_secret(
                Path::new("/run/agent-opencode"),
                "env",
                "agent-opencode:users",
                env_line("OPENROUTER_API_KEY").as_bytes(),
            ),
            other => Err(format!("unknown credential name: {other}")),
        }
    }

    // -----------------------------------------------------------------------
    // Provider execution. The VM is the sandbox: everything auto-approved.

    fn execute(&self, executor: &Executor, meta: &TaskMeta, body: &str, hint: &str) -> Result<i32> {
        let config = &self.config;
        let body = trim_trailing_newlines(body);
        let hint = trim_trailing_newlines(hint);
        let combined = format!("{hint}\n\n{body}");
        let report = || {
            let path = config.task("report.md");
            remove_any(&path)?;
            create_new(&path, 0o644)
        };
        let log = || {
            let path = config.task("agent.log");
            remove_any(&path)?;
            create_new(&path, 0o644)
        };
        let status = match executor.kind {
            ExecutorKind::Claude => {
                let mut command = runuser_command(executor.user, executor.home);
                command.arg(&config.exec_claude);
                command.args(["-p", body, "--model", &meta.model]);
                if !meta.effort.is_empty() {
                    command.args(["--effort", &meta.effort]);
                }
                command.arg("--dangerously-skip-permissions");
                command.args(["--append-system-prompt", hint]);
                command
                    .stdin(Stdio::null())
                    .stdout(report()?)
                    .stderr(log()?)
                    .status()
            }
            ExecutorKind::Codex => {
                // No system-prompt flag; the hint rides atop the prompt.
                // stdout is the session transcript (-> agent.log); the final
                // message is the report, written by the executor itself.
                let mut command = runuser_command(executor.user, executor.home);
                command.arg(&config.exec_codex);
                command.args([
                    "exec",
                    "--dangerously-bypass-approvals-and-sandbox",
                    "--skip-git-repo-check",
                    "--model",
                    &meta.model,
                ]);
                if !meta.effort.is_empty() {
                    command
                        .arg("-c")
                        .arg(format!("model_reasoning_effort={}", meta.effort));
                }
                command.arg("--output-last-message");
                command.arg(config.task("report.md"));
                command.arg(&combined);
                let transcript = log()?;
                let errors = transcript
                    .try_clone()
                    .map_err(|error| format!("clone agent.log handle: {error}"))?;
                command
                    .stdin(Stdio::null())
                    .stdout(transcript)
                    .stderr(errors)
                    .status()
            }
            ExecutorKind::Opencode | ExecutorKind::Local => {
                // stdout is the final response (-> report.md); --print-logs
                // puts the session log on stderr (-> agent.log). effort maps
                // to --variant (provider-specific reasoning effort).
                let executable = if executor.kind == ExecutorKind::Local {
                    &config.exec_local
                } else {
                    &config.exec_opencode
                };
                let mut command = runuser_command(executor.user, executor.home);
                command.arg(executable);
                command.args(["run", "--auto", "--print-logs", "--model", &meta.model]);
                if !meta.effort.is_empty() {
                    command.args(["--variant", &meta.effort]);
                }
                command.arg(&combined);
                command
                    .stdin(Stdio::null())
                    .stdout(report()?)
                    .stderr(log()?)
                    .status()
            }
        };
        status
            .map(exit_code_of)
            .map_err(|error| format!("run executor: {error}"))
    }

    // -----------------------------------------------------------------------
    // Final publication.

    fn lock_exchange(&self) -> Result<()> {
        // The supervisor always runs as guest root in production; the uid
        // check only lets the mode half of the lockdown be exercised by
        // unprivileged tests (chown to root is meaningless without root).
        if running_as_root() {
            std::os::unix::fs::chown(&self.config.task_dir, Some(0), Some(0))
                .map_err(|error| format!("chown task exchange: {error}"))?;
        }
        set_mode(&self.config.task_dir, 0o750)
    }

    fn capture_patch(&self, executor: &Executor, baseline: &str) {
        // Best-effort, as the unprivileged user, before the exchange locks.
        let _ = runuser_command(executor.user, executor.home)
            .args(["git", "-C"])
            .arg(&self.config.workspace)
            .args(["add", "--intent-to-add", "--all"])
            .stdout(Stdio::null())
            .stderr(Stdio::null())
            .status();
        let temporary = self.config.task(".changes.patch.tmp");
        // The executor can create names in the exchange until lockdown. Remove
        // any planted entry, then use create_new+O_NOFOLLOW so a replacement
        // race can only suppress the patch, never redirect a root write.
        let _ = remove_any(&temporary);
        let produced = create_new(&temporary, 0o600).ok().and_then(|output| {
            runuser_command(executor.user, executor.home)
                .args(["git", "-C"])
                .arg(&self.config.workspace)
                .args(["diff", "--binary", "--no-ext-diff", baseline])
                .stdout(output)
                .stderr(Stdio::null())
                .status()
                .ok()
        });
        if produced.is_some_and(|status| status.success()) {
            let _ = fs::rename(&temporary, self.config.task("changes.patch"));
        } else {
            let _ = fs::remove_file(&temporary);
        }
    }

    fn publish_final<F: FnOnce()>(
        &self,
        outcome: &Outcome,
        executor: Option<&Executor>,
        context: &ContextInfo,
        before_exit_code: F,
    ) -> Result<()> {
        let git_present = self.config.workspace.join(".git").is_dir();
        let steps = final_steps(executor.is_some(), !context.baseline.is_empty(), git_present);
        let mut trusted_ready = false;
        let mut before_exit_code = Some(before_exit_code);
        for step in steps {
            match step {
                FinalStep::CapturePatch => {
                    if let Some(executor) = executor {
                        self.capture_patch(executor, &context.baseline);
                    }
                }
                FinalStep::LockExchange => self.lock_exchange()?,
                FinalStep::KillExecutor => {
                    if let Some(executor) = executor {
                        // pkill exits 1 when no process remains, which is
                        // also the desired state.
                        let _ = Command::new("pkill")
                            .args(["-KILL", "-u", executor.user])
                            .status();
                    }
                }
                FinalStep::ResetTrusted => {
                    let trusted = self.config.task(".trusted");
                    match remove_any(&trusted).and_then(|()| ensure_dir(&trusted, 0o700, None)) {
                        Ok(()) => trusted_ready = true,
                        Err(error) => {
                            eprintln!("could not reset trusted output directory: {error}")
                        }
                    }
                }
                FinalStep::PublishUsage => {
                    if trusted_ready
                        && let Some(executor) = executor
                        && let Some(usage) = extract_usage(executor)
                    {
                        let _ = write_replace(
                            &self.config.task(".trusted/usage.json"),
                            format!("{usage}\n").as_bytes(),
                            0o600,
                        );
                    }
                }
                FinalStep::PublishExitCode => {
                    if let Some(stop) = before_exit_code.take() {
                        stop();
                    }
                    write_replace(
                        &self.config.task("exit-code"),
                        format!("{}\n", outcome.exit_code()).as_bytes(),
                        0o644,
                    )?;
                }
            }
        }
        Ok(())
    }

    fn write_rejection(&self, message: &str) -> Result<()> {
        write_replace(&self.config.task("report.md"), message.as_bytes(), 0o644)?;
        write_replace(&self.config.task("agent.log"), message.as_bytes(), 0o644)
    }

    // -----------------------------------------------------------------------
    // Main task flow.

    fn run(&self) -> Result<()> {
        let config = &self.config;
        // Tell the host drainer that this guest has mounted its task share
        // and reached the delivery wait loop; refreshing it every poll is the
        // idle heartbeat the drainer requires to be FRESH before claiming.
        // Poll by READING the directory, not by statting a single
        // never-existed filename: a bare stat can sit on a stale negative
        // dentry and never see the host's delivered file.
        let prompt = config.task("prompt.md");
        loop {
            touch(&config.task(".ready"))?;
            let _ = fs::read_dir(&config.task_dir).map(|entries| entries.count());
            if is_regular_nofollow(&prompt) {
                break;
            }
            thread::sleep(Duration::from_secs(1));
        }

        // Host-generated canonical metadata lives in the read-only credential
        // share; the guest never reparses executor fields from the prompt.
        let meta_path = config.creds_dir.join("task-meta");
        let meta = if is_regular_nofollow(&meta_path) {
            read_bounded(&meta_path, META_MAX_BYTES)
                .and_then(|text| parse_task_meta(&text))
                .ok()
        } else {
            None
        };
        let (meta, mut credential_error) = match meta {
            Some(meta) => (meta, false),
            None => (TaskMeta::default(), true),
        };

        let executor = select_executor(&meta.agent, &meta.model);
        let rule = credential_rule(&meta.agent, &meta.model);
        if !credentials_match(&self.credential_entries(), rule) {
            credential_error = true;
        }
        if !credential_error
            && !meta.model.is_empty()
            && let Err(error) = self.install_credential(rule)
        {
            eprintln!("credential installation failed: {error}");
            credential_error = true;
        }

        // Heartbeat covers context preparation as well as model work, so a
        // failed/slow capsule never looks like a dead VM.
        let heartbeat_path = config.task(".heartbeat");
        let (stop_heartbeat, heartbeat_stopped) = mpsc::channel::<()>();
        let heartbeat = thread::spawn(move || {
            loop {
                let _ = touch(&heartbeat_path);
                match heartbeat_stopped.recv_timeout(Duration::from_secs(15)) {
                    Err(mpsc::RecvTimeoutError::Timeout) => {}
                    _ => break,
                }
            }
        });

        let mut context = ContextInfo::default();
        let mut context_error = None;
        if !credential_error
            && let Some(executor) = &executor
            && is_regular_nofollow(&config.task("context.tar.zst"))
        {
            let mut log = String::new();
            match self.prepare_context(executor, &mut log) {
                Ok(info) => context = info,
                Err(error) => {
                    log.push_str(&error);
                    log.push('\n');
                    context_error = Some(log);
                }
            }
        }

        let outcome = if credential_error {
            Outcome::CredentialRejected
        } else if let Some(log) = context_error {
            Outcome::ContextFailed(log)
        } else if let Some(executor) = executor.as_ref().filter(|_| !meta.model.is_empty()) {
            let execution = read_bounded(&prompt, PROMPT_MAX_BYTES).and_then(|prompt_text| {
                let body = strip_front_matter(&prompt_text);
                let hint = read_bounded(&config.hint_file, HINT_MAX_BYTES)?;
                self.execute(executor, &meta, &body, &hint)
            });
            match execution {
                Ok(code) => Outcome::Ran(code),
                Err(error) => Outcome::InternalFailed(error),
            }
        } else {
            Outcome::MetaRejected
        };
        if let Some(message) = outcome.message()
            && let Err(error) = self.write_rejection(&message)
        {
            eprintln!("could not publish failure report: {error}");
        }

        self.publish_final(&outcome, executor.as_ref(), &context, || {
            drop(stop_heartbeat);
            let _ = heartbeat.join();
        })
    }
}

// std-only effective-uid probe: /proc/self is owned by the process euid.
fn running_as_root() -> bool {
    fs::metadata("/proc/self")
        .map(|metadata| metadata.uid() == 0)
        .unwrap_or(false)
}

fn env_path(name: &str) -> Result<PathBuf> {
    env::var_os(name)
        .filter(|value| !value.is_empty())
        .map(PathBuf::from)
        .ok_or_else(|| format!("required environment variable {name} is missing"))
}

fn env_path_or(name: &str, fallback: &str) -> PathBuf {
    env::var_os(name)
        .filter(|value| !value.is_empty())
        .map(PathBuf::from)
        .unwrap_or_else(|| PathBuf::from(fallback))
}

fn main() {
    let result = Config::from_env().and_then(|config| Supervisor { config }.run());
    if let Err(error) = result {
        eprintln!("fleet-guest-supervisor: {error}");
        std::process::exit(1);
    }
}

