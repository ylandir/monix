// The exact invocation shape the Bash supervisor used for unprivileged work:
// runuser -u <user> -- env HOME=<home> <program> <args...>.
fn runuser_command(user: &str, home: &str) -> Command {
    let mut command = Command::new("runuser");
    command.args(["-u", user, "--", "env"]);
    command.arg(format!("HOME={home}"));
    command
}

struct BoundedOutput {
    status: std::process::ExitStatus,
    stdout: Vec<u8>,
    stderr: Vec<u8>,
}

fn drain_bounded(mut reader: impl Read, limit: usize) -> Vec<u8> {
    let mut kept = Vec::new();
    let mut buffer = [0_u8; 8192];
    while let Ok(read) = reader.read(&mut buffer) {
        if read == 0 {
            break;
        }
        let remaining = limit.saturating_sub(kept.len());
        kept.extend_from_slice(&buffer[..read.min(remaining)]);
    }
    kept
}

fn command_output_bounded(command: &mut Command, limit: usize) -> Result<BoundedOutput> {
    command.stdout(Stdio::piped()).stderr(Stdio::piped());
    let mut child = command
        .spawn()
        .map_err(|error| format!("run {command:?}: {error}"))?;
    let stdout = child
        .stdout
        .take()
        .ok_or_else(|| "command stdout pipe is missing".to_string())?;
    let stderr = child
        .stderr
        .take()
        .ok_or_else(|| "command stderr pipe is missing".to_string())?;
    let stdout_reader = thread::spawn(move || drain_bounded(stdout, limit));
    let stderr_reader = thread::spawn(move || drain_bounded(stderr, limit));
    let status = child
        .wait()
        .map_err(|error| format!("wait for {command:?}: {error}"))?;
    let stdout = stdout_reader
        .join()
        .map_err(|_| "command stdout reader panicked".to_string())?;
    let stderr = stderr_reader
        .join()
        .map_err(|_| "command stderr reader panicked".to_string())?;
    Ok(BoundedOutput {
        status,
        stdout,
        stderr,
    })
}

fn append_bounded(log: &mut String, bytes: &[u8]) {
    let remaining = COMMAND_OUTPUT_MAX_BYTES.saturating_sub(log.len());
    if remaining > 0 {
        log.push_str(&String::from_utf8_lossy(
            &bytes[..bytes.len().min(remaining)],
        ));
    }
}

// Run a command, appending stdout+stderr to a preparation log; Err on
// nonzero exit so context preparation aborts like `|| return` did.
fn run_logged(command: &mut Command, log: &mut String) -> Result<()> {
    let output = command_output_bounded(command, COMMAND_OUTPUT_MAX_BYTES)?;
    append_bounded(log, &output.stdout);
    append_bounded(log, &output.stderr);
    if !output.status.success() {
        return Err(format!("{command:?} exited {}", output.status));
    }
    Ok(())
}

fn run_captured(command: &mut Command, log: &mut String) -> Result<String> {
    let output = command_output_bounded(command, COMMAND_OUTPUT_MAX_BYTES)?;
    append_bounded(log, &output.stderr);
    if !output.status.success() {
        append_bounded(log, &output.stdout);
        return Err(format!("{command:?} exited {}", output.status));
    }
    Ok(String::from_utf8_lossy(&output.stdout).into_owned())
}

fn exit_code_of(status: std::process::ExitStatus) -> i32 {
    status
        .code()
        .or_else(|| status.signal().map(|signal| 128 + signal))
        .unwrap_or(1)
}

// Pipe text through a fixed jq program; best-effort — None on any failure
// or empty output, exactly like the previous `... | jq ... || usage=""`.
fn tool_command(executor: Option<&Executor>, program: &str) -> Command {
    if let Some(executor) = executor {
        let mut command = runuser_command(executor.user, executor.home);
        command.arg(program);
        command
    } else {
        Command::new(program)
    }
}

fn jq_filter(args: &[&str], stdin_text: &str, executor: Option<&Executor>) -> Option<String> {
    let mut child = tool_command(executor, "jq")
        .args(args)
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .stderr(Stdio::null())
        .spawn()
        .ok()?;
    // A jq that stops reading must not deadlock the writer: feed stdin from
    // a thread while draining stdout here.
    let mut stdin = child.stdin.take()?;
    let text = stdin_text.to_string();
    let writer = thread::spawn(move || {
        let _ = stdin.write_all(text.as_bytes());
    });
    let output = child.wait_with_output().ok()?;
    let _ = writer.join();
    let result = String::from_utf8_lossy(&output.stdout)
        .trim_end()
        .to_string();
    (output.status.success() && !result.is_empty()).then_some(result)
}

fn concatenate_files(paths: &[PathBuf]) -> String {
    let mut text = String::new();
    for path in paths {
        let remaining = USAGE_MAX_BYTES.saturating_sub(text.len() as u64);
        if remaining == 0 {
            break;
        }
        if let Ok(file) = open_nofollow(path) {
            let _ = file.take(remaining).read_to_string(&mut text);
        }
    }
    text
}

