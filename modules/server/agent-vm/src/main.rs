// Filesystem helpers: symlink-refusing, bounded.

fn open_nofollow(path: &Path) -> io::Result<File> {
    OpenOptions::new()
        .read(true)
        .custom_flags(O_NOFOLLOW | O_CLOEXEC)
        .open(path)
}

fn is_regular_nofollow(path: &Path) -> bool {
    fs::symlink_metadata(path)
        .map(|metadata| metadata.file_type().is_file())
        .unwrap_or(false)
}

fn read_bounded(path: &Path, limit: u64) -> Result<String> {
    let file = open_nofollow(path).map_err(|error| format!("open {}: {error}", path.display()))?;
    let metadata = file
        .metadata()
        .map_err(|error| format!("inspect {}: {error}", path.display()))?;
    if !metadata.file_type().is_file() || metadata.len() > limit {
        return Err(format!("unsafe or oversized file: {}", path.display()));
    }
    let mut text = String::new();
    file.take(limit.saturating_add(1))
        .read_to_string(&mut text)
        .map_err(|error| format!("read {}: {error}", path.display()))?;
    if text.len() as u64 > limit {
        return Err(format!("file grew beyond bound: {}", path.display()));
    }
    Ok(text)
}

fn touch(path: &Path) -> Result<()> {
    let file = OpenOptions::new()
        .create(true)
        .truncate(false)
        .write(true)
        .custom_flags(O_NOFOLLOW | O_CLOEXEC | O_NONBLOCK)
        .open(path)
        .map_err(|error| format!("touch {}: {error}", path.display()))?;
    if !file
        .metadata()
        .map_err(|error| format!("inspect {}: {error}", path.display()))?
        .file_type()
        .is_file()
    {
        return Err(format!(
            "refusing to touch non-regular file: {}",
            path.display()
        ));
    }
    file.set_modified(SystemTime::now())
        .map_err(|error| format!("touch {}: {error}", path.display()))
}

fn set_mode(path: &Path, mode: u32) -> Result<()> {
    fs::set_permissions(path, fs::Permissions::from_mode(mode))
        .map_err(|error| format!("chmod {:04o} {}: {error}", mode, path.display()))
}

// Ownership by account name goes through chown(1) so the supervisor never
// hand-parses the user database.
fn chown_name(path: &Path, owner: &str) -> Result<()> {
    let status = Command::new("chown")
        .arg(owner)
        .arg(path)
        .status()
        .map_err(|error| format!("run chown for {}: {error}", path.display()))?;
    if !status.success() {
        return Err(format!("chown {owner} {} failed", path.display()));
    }
    Ok(())
}

fn ensure_dir(path: &Path, mode: u32, owner: Option<&str>) -> Result<()> {
    fs::create_dir_all(path).map_err(|error| format!("mkdir {}: {error}", path.display()))?;
    set_mode(path, mode)?;
    if let Some(owner) = owner {
        chown_name(path, owner)?;
    }
    Ok(())
}

fn write_new(path: &Path, contents: &[u8], mode: u32) -> Result<()> {
    let mut file = OpenOptions::new()
        .write(true)
        .create_new(true)
        .mode(mode)
        .custom_flags(O_NOFOLLOW | O_CLOEXEC)
        .open(path)
        .map_err(|error| format!("create {}: {error}", path.display()))?;
    file.write_all(contents)
        .and_then(|()| file.sync_all())
        .map_err(|error| format!("write {}: {error}", path.display()))
}

fn create_new(path: &Path, mode: u32) -> Result<File> {
    OpenOptions::new()
        .write(true)
        .create_new(true)
        .mode(mode)
        .custom_flags(O_NOFOLLOW | O_CLOEXEC | O_NONBLOCK)
        .open(path)
        .map_err(|error| format!("create {}: {error}", path.display()))
}

fn remove_any(path: &Path) -> Result<()> {
    match fs::symlink_metadata(path) {
        Err(_) => Ok(()),
        Ok(metadata) if metadata.file_type().is_dir() => {
            fs::remove_dir_all(path).map_err(|error| format!("remove {}: {error}", path.display()))
        }
        Ok(_) => {
            fs::remove_file(path).map_err(|error| format!("remove {}: {error}", path.display()))
        }
    }
}

fn write_replace(path: &Path, contents: &[u8], mode: u32) -> Result<()> {
    remove_any(path)?;
    write_new(path, contents, mode)
}

// Sorted regular (non-symlink) files in one directory whose name matches.
fn matching_files(directory: &Path, matches: impl Fn(&str) -> bool) -> Vec<PathBuf> {
    let Ok(entries) = fs::read_dir(directory) else {
        return Vec::new();
    };
    let mut paths: Vec<PathBuf> = entries
        .flatten()
        .map(|entry| entry.path())
        .filter(|path| {
            path.file_name()
                .and_then(|name| name.to_str())
                .is_some_and(&matches)
                && is_regular_nofollow(path)
        })
        .collect();
    paths.sort();
    paths
}

// Recursive walk collecting regular *.suffix files; never follows symlinks.
fn walk_files(directory: &Path, suffix: &str, into: &mut Vec<PathBuf>, depth: usize) {
    if depth >= USAGE_MAX_DEPTH || into.len() >= USAGE_MAX_FILES {
        return;
    }
    let Ok(entries) = fs::read_dir(directory) else {
        return;
    };
    let mut paths: Vec<PathBuf> = entries.flatten().map(|entry| entry.path()).collect();
    paths.sort();
    for path in paths {
        let Ok(metadata) = fs::symlink_metadata(&path) else {
            continue;
        };
        if metadata.file_type().is_dir() {
            walk_files(&path, suffix, into, depth + 1);
        } else if metadata.file_type().is_file()
            && path
                .file_name()
                .and_then(|name| name.to_str())
                .is_some_and(|name| name.ends_with(suffix))
        {
            into.push(path);
            if into.len() >= USAGE_MAX_FILES {
                return;
            }
        }
    }
}

